# Operadores Matemáticos

ValorA = 99
ValorB = 5

print()

print("Variáveis:\nValor A:", ValorA, "- Valor B:", ValorB)
print()
print("Soma dos valores         :",ValorA + ValorB)
print("Subtração dos valores    :", ValorA - ValorB)
print("Multiplicação dos valores:", ValorA * ValorB)
print("Elevar a uma potência    :", ValorA ** ValorB)
print("Divisão de valores       :", ValorA / ValorB)
print("Dividir truncando        :", ValorA // ValorB) # Sem casas decimais
print("MOD ou resto de divisão  :", ValorA % ValorB)
print()

